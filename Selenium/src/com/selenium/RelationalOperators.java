package com.selenium;

public class RelationalOperators {

	public static void main(String[] args) {
		
		int a = 20;
		int b = 30;
		
		System.out.println("Using less than symbol  : "  + (a<b));
		System.out.println("Using Greater than symbol  : "   + (b>a));
		System.out.println("Using less than or equal to symbol  : "   + (a<=b));
		System.out.println("Using less than or equal to symbol  : "   + (b<=a));
		System.out.println("Using condition equal to symbol  : "   + (a==b));
		System.out.println("Using condition not equal to symbol  : "   + (a!=b));
		
		
		
	}
	
	

}
